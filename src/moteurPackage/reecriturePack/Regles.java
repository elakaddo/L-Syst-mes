package moteurPackage.reecriturePack;


/**
* Class Regles est l'ensemble de regles a respecter pour reecrire
 * @author Akaddar Mohamed
 * Date 24/01/2022
 */
public class Regles {


	private String lettre;
	private String lettreT;

    /**
     * @param a le caractere initial.
     * @param b le caractere apres la transformation
     * */

	public Regles(String lettre, String lettreT)
	{
		this.lettre = lettre;
		this.lettreT = lettreT;
	}

	/**
	 * @param l lettre a tester
	 * @return Si la regle est appliquee
	 * */
	public boolean estAppliquee(Character l)
	{
		return (l.toString().equals(this.lettre));
	}

	/**
	 * @return lettre */
	public String getLettre()
	{
		return this.lettre;
	}


	/**
	 * @return lettre transformee */
	public String getLettreT()
	{
		return this.lettreT;
	}





}
